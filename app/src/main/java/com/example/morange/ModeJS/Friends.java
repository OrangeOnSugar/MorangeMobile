package com.example.morange.ModeJS;

public class Friends {

    private String добавил;
    private String добавленный;

    public Friends(String добавил, String добавленный) {
        this.добавил = добавил;
        this.добавленный = добавленный;
    }

    public Friends() {
    }

    public String getДобавил() {
        return добавил;
    }

    public void setДобавил(String добавил) {
        this.добавил = добавил;
    }

    public String getДобавленный() {
        return добавленный;
    }

    public void setДобавленный(String добавленный) {
        this.добавленный = добавленный;
    }
}

